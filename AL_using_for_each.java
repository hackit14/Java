import java.util.*;

public class AL_using_for_each {

    public static void main(String args[])
    {
        ArrayList<String> str = new ArrayList<>();

        str.add("ME");
        str.add("YOU");
        str.add("US");
        str.add("WE");

        for(String a:str)
        {
            System.out.println(a);

        }


    }
    
}
